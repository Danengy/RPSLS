class Player:
    def __init__(self, name):
        self.name = name 
        self.gestures = ["Rock", "Paper", "Scissor", "Lizard", "Spock"]
        self.score = 0

    def choose_gesture(self):
        user = Player()
        user.gestures[0] = 0
        user.gestures[1] = 1
        user.gestures[2] = 2
        user.gestures[3] = 3
        user.gestures[4] = 4



